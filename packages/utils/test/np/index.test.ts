test('NP.NumberCalculator supports chain operations', () => {
  const calc = NP.createCalculator(10)

  expect(calc.plus(0.1).valueOf()).toBe(10.1)
  expect(calc.minus(0.2).valueOf()).toBe(9.9)
  expect(calc.times(2).valueOf()).toBe(19.8)
  expect(calc.divide(2).round(1).valueOf()).toBe(9.9)

  expect(String(calc)).toBe('9.9')

  const complex = NP.createCalculator(1.23456)
    .plus(0.1)
    .minus(0.2)
    .times(2)
    .divide(0.3)
    .round(2)
    .valueOf()

  expect(complex).toBe(7.56)
})
