import { afterEach, beforeEach, expect, test } from 'vitest'
import NP from '@/np/index'

beforeEach(() => {
  // Disable boundary checking before each test
  NP.enableBoundaryChecking(false)
})

afterEach(() => {
  // Restore boundary checking after each test
  NP.enableBoundaryChecking(true)
})
test('NP.strip can eliminate rounding errors', () => {
  expect(NP.strip(0.09999999999999998)).toBe(0.1)
  expect(NP.strip(1.0000000000000001)).toBe(1)
  expect(NP.strip('0.09999999999999998')).toBe(0.1)
  expect(NP.strip('1.0000000000000001')).toBe(1)
})

