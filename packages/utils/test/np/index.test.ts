test('NP.plus can do plus operation', () => {
  expect(NP.plus(0.1, 0.2)).toBe(0.3)
  expect(NP.plus(2.3, 2.4)).toBe(4.7)
  expect(NP.plus(-1.6, -1)).toBe(-2.6)
  expect(NP.plus(-2.0, 63)).toBe(61)
  expect(NP.plus(-3, 7)).toBe(4)
  expect(NP.plus(-221, 38)).toBe(-183)
  expect(NP.plus(-1, 0)).toBe(-1)
  expect(NP.plus(2.018, 0.001)).toBe(2.019)
  expect(NP.plus(1.3224e10, 1.3224e3)).toBe(13224001322.4)
  expect(NP.plus(1.6e-30, 1.6e-30)).toBe(3.2e-30)
  expect(NP.plus('0.1', '0.2')).toBe(0.3)
  expect(NP.plus('2.3', '2.4')).toBe(4.7)
  expect(NP.plus('-1.6', '-1')).toBe(-2.6)
  expect(NP.plus('-2.0', '63')).toBe(61)
  expect(NP.plus('-3', '7')).toBe(4)
  expect(NP.plus('-221', '38')).toBe(-183)
  expect(NP.plus('-1', '0')).toBe(-1)
  expect(NP.plus('-1', '0', '2', '3', 4)).toBe(8)
  expect(NP.plus('2.018', '0.001')).toBe(2.019)
  expect(NP.plus('1.3224e10', '1.3224e3')).toBe(13224001322.4)
  expect(NP.plus('1.6e-30', '1.6e-30')).toBe(3.2e-30)

  expect(NP.plus(0.1, 0.2, 0.3)).toBe(0.6)
  expect(NP.plus('0.1', '0.2', '0.3')).toBe(0.6)

  expect(
    NP.plus(...(new Array(500).fill(1) as [number, number, ...number[]])),
  ).toBe(500)
})

test('NP.minus can do minus operation', () => {
  expect(NP.minus(0.07, 0.01)).toBe(0.06)
  expect(NP.minus(0.7, 0.1)).toBe(0.6)
  expect(NP.minus(1.0, 0.9)).toBe(0.1)
  expect(NP.minus(1, 0)).toBe(1)
  expect(NP.minus(1, -0)).toBe(1)
  expect(NP.minus(-1, 0)).toBe(-1)
  expect(NP.minus(-1, -0)).toBe(-1)
  expect(NP.minus(1, 22)).toBe(-21)
  expect(NP.minus(8893568.397103781249, -7.2967405955)).toBe(
    8893575.693844376749,
  )
  expect(NP.minus(105468873, 0)).toBe(105468873)
  expect(NP.minus('0.07', '0.01')).toBe(0.06)
  expect(NP.minus('0.7', '0.1')).toBe(0.6)
  expect(NP.minus('1.0', '0.9')).toBe(0.1)
  expect(NP.minus('1', '0')).toBe(1)
  expect(NP.minus('1', '-0')).toBe(1)
  expect(NP.minus('-1', '0')).toBe(-1)
  expect(NP.minus('-1', '-0')).toBe(-1)
  expect(NP.minus('1', '22')).toBe(-21)
  expect(NP.minus('8893568.397103781249', '-7.29674059550')).toBe(
    8893575.693844376749,
  )
  expect(NP.minus('105468873', '0')).toBe(105468873)

  expect(NP.minus(1.23e5, 10)).toBe(122990)
  expect(NP.minus(1.23e-5, 1.0023)).toBe(-1.0022877)
  expect(NP.minus(1.3224e10, 21)).toBe(13223999979)
  expect(NP.minus(1.3224e10, 1.3224e3)).toBe(13223998677.6)
  expect(NP.minus(1.7e-30, 0.1e-30)).toBe(1.6e-30)
  expect(NP.minus('1.23e5', '10')).toBe(122990)
  expect(NP.minus('1.23e-5', '1.0023')).toBe(-1.0022877)
  expect(NP.minus('1.3224e10', '21')).toBe(13223999979)
  expect(NP.minus('1.3224e10', '1.3224e3')).toBe(13223998677.6)
  expect(NP.minus('1.7e-30', '0.1e-30')).toBe(1.6e-30)

  expect(NP.minus(6, 3, 2)).toBe(1)
  expect(NP.minus(6, 3, 2, 1, 2, 3)).toBe(-5)
  expect(NP.minus('6', '3', '2')).toBe(1)
  expect(NP.minus('6', '3', '2', '1', '2', '3')).toBe(-5)

  expect(
    NP.minus(500, ...(new Array(500).fill(1) as [number, ...number[]])),
  ).toBe(0)
})

